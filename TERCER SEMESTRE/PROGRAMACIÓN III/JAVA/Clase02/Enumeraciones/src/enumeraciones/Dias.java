/*
    Una enumeración (enum) es un tipo similar a la clase, pero es una clase 
    particular que contiene varios elementos definidos dentro de esta enumeración.

    La enumeración permite crear con mayor facilidad las constantes que se 
    desean enumerar. Es decir, es más fácil crear una lista de valores constantes.
*/

package enumeraciones;

// Creación de enum
public enum Dias {
    // Definición de constantes
    LUNES,
    MARTES,
    MIERCOLES,
    JUEVES,
    VIERNES,
    SABADO,
    DOMINGO;
}
