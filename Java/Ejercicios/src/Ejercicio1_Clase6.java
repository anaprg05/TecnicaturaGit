
// Ejercicio 1:
//  Indicar si el usuario es mayor de edad o no.

public class Ejercicio1_Clase6 {
    public static void main(String[] args) {
        var edad = 15;
        var adulto = edad >= 18;
        if (adulto){
            System.out.println("Usted es mayor de edad.");
        }
        else{
            System.out.println("Usted es menor de edad.");
        }
    }
}
