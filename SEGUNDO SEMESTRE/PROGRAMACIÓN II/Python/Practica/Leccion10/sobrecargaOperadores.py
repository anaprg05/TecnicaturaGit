# Sobrecarga de operadores 
# Un mismo operador puede comportarse de diferentes formas

# Ejemplo: Operador de suma (+)
# En el primer caso, está sumando dos numéricos
a = 3
b = 5
print(a + b)

# En el segundo caso, está concatenando valores tipo String
a = 'Hola '
b = 'mundo'
print(a + b)

# En el tercer caso, está uniendo valores de tipo lista
a = [3, 4, 5]
b = [6, 7, 8, 9]
print(a + b)
